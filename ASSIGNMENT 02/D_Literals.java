public class D_Literals
{
    public static void main(String[] args)
    {
        int i= 45;
        float f= 24.56f;
        boolean b= true;
        String str= "Alok";
        char ch= 'a';
        double d= 78.4e5;
        System.out.println("Integer literal: "+i);
        System.out.println("Floating point literal: "+f);
        System.out.println("Boolean literal: "+b);
        System.out.println("String literal: "+str);
        System.out.println("Character literal: "+ch);
        System.out.println("Decimal literal: "+d);
    }
}
